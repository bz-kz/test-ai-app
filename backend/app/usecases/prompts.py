"""プロンプトビルダー: カルテ下書き生成用のプロンプトを構築する。

このモジュールは純粋関数のみを含む。LLM 呼び出し・ログ出力・副作用は一切行わない。
"""

from __future__ import annotations

# システムプロンプト定数。
# トークン予算: gemma4:e4b のコンテキスト上限を考慮し、システムブロック自体を
# 約200トークン以内に収める。clinical_input が最大 ~3600トークン分を占められるよう余裕を確保する。
# (SPEC.md#inference-layer-contract: 合計プロンプト ≤4k tokens)
DRAFT_SYSTEM_PROMPT = """あなたは経験豊富な医師のアシスタントです。
提供された臨床情報をもとに、日本語の SOAP 形式のカルテ下書きを作成してください。

出力形式:
【主訴・現病歴 (Subjective)】
患者の訴えと症状の経過を記載する。

【身体所見・検査結果 (Objective)】
バイタルサイン、身体診察所見、検査結果を記載する。

【評価 (Assessment)】
診断名または鑑別診断を記載する。

【治療計画 (Plan)】
投薬、処置、フォローアップ計画を記載する。

注意: 提供された情報のみをもとに作成し、不明な項目は「情報なし」と記載してください。"""


def build_draft_prompt(clinical_input: str) -> str:
    """臨床入力からカルテ下書き生成用プロンプトを構築する。

    この関数は決定論的であり、同じ入力に対して常に同じ出力を返す。
    ログ出力・LLM 呼び出し・副作用は一切行わない。

    Args:
        clinical_input: 臨床医が入力した診療メモ（PHI を含む場合がある）

    Returns:
        LLM に送信するプロンプト文字列。
        PHI はローカルモデルへのプロンプト本文に含まれることが許可されている
        (local-llm-and-phi.md §3)。
    """
    return (
        f"{DRAFT_SYSTEM_PROMPT}\n\n"
        f"---\n"
        f"臨床情報:\n{clinical_input}\n"
        f"---\n\n"
        f"上記の臨床情報をもとに、SOAP 形式のカルテ下書きを日本語で作成してください。"
    )
